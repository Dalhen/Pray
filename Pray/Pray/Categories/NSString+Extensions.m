+ (NSString *)randomTitle {
    NSString *firstWord = [self randomFirstWord];
    NSString *secondWord = [self randomWord];
    NSString *thirdWord = [self randomThirdWord];
    int chance = arc4random() % 5;
    
    if ([firstWord isEqualToString:@""])
    {
        if (chance == 0)
        {
            return [NSString stringWithFormat:@"%@", thirdWord];
        }
        return [NSString stringWithFormat:@"%@ %@", secondWord, thirdWord];
    }
    
    
    if (chance == 0)
    {
        if ([firstWord isEqualToString:@"A"] &&
            ([thirdWord.uppercaseString hasPrefix:@"A"] ||
             [thirdWord.uppercaseString hasPrefix:@"E"] ||
             [thirdWord.uppercaseString hasPrefix:@"I"] ||
             [thirdWord.uppercaseString hasPrefix:@"O"] ||
             [thirdWord.uppercaseString hasPrefix:@"U"] ))
        {
            firstWord = @"An";
        }
        return [NSString stringWithFormat:@"%@ %@", firstWord, thirdWord];
    }
    
    if ([firstWord isEqualToString:@"A"] &&
        ([secondWord.uppercaseString hasPrefix:@"A"] ||
         [secondWord.uppercaseString hasPrefix:@"E"] ||
         [secondWord.uppercaseString hasPrefix:@"I"] ||
         [secondWord.uppercaseString hasPrefix:@"O"] ||
         [secondWord.uppercaseString hasPrefix:@"U"] ))
    {
        firstWord = @"An";
    }
    return [NSString stringWithFormat:@"%@ %@ %@", firstWord, secondWord, [self randomThirdWord]];
}

