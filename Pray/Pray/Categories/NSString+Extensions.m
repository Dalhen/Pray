+ (NSString *)lipsumWithParagraphs:(NSUInteger)numberOfParagraphs
{
    if (numberOfParagraphs <=0)
    {
        printImportant(@"cannot generate lipsum with no paragraphs");
        return nil;
    }
    
    NSArray *lipsumWords = [self lipsumWords];
    
    NSMutableString *lipsum = [NSMutableString stringWithString:@"    Lorem ipsum dolor sit amet "];
    for (NSUInteger i = 0; i < numberOfParagraphs; i++)
    {
        NSUInteger numberOfSentences = [JXRandomizer randomIntBetweenMinInt:5 andMaxInt:30];
        for (NSUInteger j = 0; j < numberOfSentences; j++)
        {
            NSUInteger numberOfWords = [JXRandomizer randomIntBetweenMinInt:5 andMaxInt:25];
            int coma1Position = [JXRandomizer randomIntBetweenMinInt:-20 andMaxInt:20];
            int coma2Postion = [JXRandomizer randomIntBetweenMinInt:-20 andMaxInt:20];
            for (NSUInteger k = 0; k < numberOfWords; k++)
            {
                if (k == 0 && !(i == 0 && j == 0))
                {
                    [lipsum appendFormat:@"%@ ", [[lipsumWords randomObject] capitalizedString]];
                }
                else if (k == numberOfWords-1)
                {
                    [lipsum appendFormat:@"%@. ", [[lipsumWords randomObject] lowercaseString]];
                }
                else if (k == coma1Position || k == coma2Postion)
                {
                    [lipsum appendFormat:@"%@, ", [[lipsumWords randomObject] lowercaseString]];
                }
                else
                {
                    [lipsum appendFormat:@"%@ ", [[lipsumWords randomObject] lowercaseString]];
                }
            }
        }
        if (i != numberOfParagraphs-1)
        {
            [lipsum appendFormat:@"\n\n    "];
        }
    }
    return lipsum;
}

