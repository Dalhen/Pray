#pragma mark - validation
- (BOOL)isValidEmailAddress
{
    if (![self isValidString])
        {
        return NO;
        }
    
    NSString *emailRegularExpression =
    @"(?:[a-z0-9!#$%\\&'*+/=?\\^_`{|}~-]+(?:\\.[a-z0-9!#$%\\&'*+/=?\\^_`{|}"
    @"~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\"
    @"x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-"
    @"z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5"
    @"]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-"
    @"9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21"
    @"-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])";
    
    NSPredicate *regExPredicate = [NSPredicate predicateWithFormat:@"SELF MATCHES %@", emailRegularExpression];
    return [regExPredicate evaluateWithObject:self];
}

- (BOOL)isValidWebAddress
{
    if (![self isValidString])
        {
        return NO;
        }
    
    NSURL *candidateURL = [NSURL URLWithString:self];
    if (candidateURL && candidateURL.scheme && candidateURL.host)
        {
        return YES;
        }
    return NO;
}

- (BOOL)isValidPhoneNumber
{
    if (![self isValidString])
        {
        return NO;
        }
    
    NSCharacterSet * set = [[NSCharacterSet characterSetWithCharactersInString:@"1234567890-()+ *#;,"] invertedSet];
    if ([self rangeOfCharacterFromSet:set].location == NSNotFound)
        {
        return YES;
        }
    return NO;
}

#pragma mark - comparison
- (BOOL)containsString:(NSString *)string
{
    return [self rangeOfString:string options:NSCaseInsensitiveSearch].location != NSNotFound;
}

- (BOOL)hasOnlyCharactersInString:(NSString *)string
{
    return [self rangeOfCharacterFromSet:[[NSCharacterSet characterSetWithCharactersInString:string] invertedSet]].location == NSNotFound;
}

- (BOOL)hasNoCharactersInString:(NSString *)string
{
    return [self rangeOfCharacterFromSet:[NSCharacterSet characterSetWithCharactersInString:string]].location == NSNotFound;
}

#pragma mark - search
- (BOOL)isIn:(NSString *)firstArg, ... {
    NSMutableSet *items = [[NSMutableSet alloc] init];
    
    va_list args;
    va_start(args, firstArg);
    for (NSString *arg = firstArg; arg != nil; arg = va_arg(args, NSString*))
    {
        [items addObject:arg];
    }
    va_end(args);
    
    return [items containsObject:self];
}

#pragma mark - utility
- (NSString*) trim
{
    return [self stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
}


